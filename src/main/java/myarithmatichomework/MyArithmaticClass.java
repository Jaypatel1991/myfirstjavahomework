package myarithmatichomework;

public class MyArithmaticClass {
    public static void main(String[] args) {
        float a=20;
        float b=30;
        boolean b1=false;
        boolean a1=true;

        System.out.println(a+b); //addition 50
        System.out.println(a-b); //subtraction -10
        System.out.println(a*b); //multiplication 600
        System.out.println(a/b); //division 0.66
        System.out.println(a%b); // module 20
        System.out.println(a++); // 20
        System.out.println(++a); // 22
        System.out.println(b++); // 30
        System.out.println(++b); // 32
        System.out.println(!a1); // true
        System.out.println(!b1); // false
    }
}